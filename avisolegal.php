<h2 style="text-align:center;">POLÍTICA DE COOKIES</h2>

<p>La empresa informa de su política de cookies, cumpliendo así con lo previsto en el artículo 22.2 de la Ley 34/2002, de 11 de 
julio de Servicios de la Sociedad de la Información y de Comercio Electrónico.
Cookie es un fichero que se descarga en su ordenador al acceder a determinadas páginas web. Las cookies permiten a una página web, 
entre otras cosas, almacenar y recuperar información sobre los hábitos de navegación de un usuario o de su equipo y, dependiendo de 
la información que contengan y de la forma en que utilice su equipo, pueden utilizarse para reconocer al usuario. El navegador del 
usuario memoriza cookies en el disco duro solamente durante la sesión actual ocupando un espacio de memoria mínimo y no perjudicando 
su ordenador. Las cookies no contienen ninguna clase de información personal específica, y la mayoría de las mismas se borran del disco 
duro al finalizar la sesión de navegador.</p>
<p>La mayoría de los navegadores aceptan como estándar a las cookies y, con independencia de las mismas, permiten o impiden en 
los ajustes de seguridad las cookies temporales o memorizadas.</p>

<P>Sin su consentimiento mediante la activación de las cookies en su navegador, no se enlazará en las cookies los datos memorizados 
con sus datos personales proporcionados en el momento del registro o la compra.</P>

